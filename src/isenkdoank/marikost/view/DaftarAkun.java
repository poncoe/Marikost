    private void btnDaftarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDaftarActionPerformed
        String username = txtUsername.getText();
        String password = String.valueOf(txtPassword.getPassword());
        String jenisakun = cboxJenisAkun.getSelectedItem().toString();
        String jeniskelamin = cboxJenisKelamin.getSelectedItem().toString();
        String nama = txtNama.getText();
        String alamat = txtAlamat.getText();
        Integer notelepon = Integer.parseInt(txtNotelp.getText());
        String email = txtEmail.getText();
        String statusbayar = "Belum";

        User objekUser = new User(username, password, jenisakun, nama, jeniskelamin, alamat, notelepon, email, statusbayar);

        if (username.equals("")) {
            JOptionPane.showMessageDialog(null, "Masukan Username!");
        } else if (password.equals("")) {
            JOptionPane.showMessageDialog(null, "Masukan Password!");
        } else if (nama.equals("")) {
            JOptionPane.showMessageDialog(null, "Masukan Nama!");
        } else if (alamat.equals("")) {
            JOptionPane.showMessageDialog(null, "Masukan Alamat!");
        } else if (notelepon.equals("")) {
            JOptionPane.showMessageDialog(null, "Masukan No Telepon!");
        } else if (email.equals("")) {
            JOptionPane.showMessageDialog(null, "Masukan Email!");
        } else if (auth.cekUsername(username)) {
            JOptionPane.showMessageDialog(null, "Username Sudah Ada / Sudah Teregistrasi!");
        } else if (btnDaftar.getText().equals("Daftar Akun")) {
            try {
                if (auth.tambahAkun(objekUser)) {
                    JOptionPane.showMessageDialog(this, "Registrasi Berhasil, Silahkan Login!");
                    new Login().setVisible(true);
                    this.dispose();
                } else {
                    JOptionPane.showMessageDialog(this, "Registrasi Gagal!");
                }
            } catch (SQLException ex) {
                Logger.getLogger(DaftarAkun.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
    }//GEN-LAST:event_btnDaftarActionPerformed

